package com.example.demo.model;

public class UserInfo {

	private int hnCount;
	
	private int hrCount;
	
	private int lnCount;
	
	private int lrCount;
	
	private int dfCount;
	
	private int nullCount;
	
	private int count;
	
	public int getHnCount() {
		return hnCount;
	}
	
	public void setHnCount(int hnCount) {
		this.hnCount = hnCount;
	}
	
	public int getHrCount() {
		return hrCount;
	}
	
	public void setHrCount(int hrCount) {
		this.hrCount = hrCount;
	}
	
	public int getLnCount() {
		return lnCount;
	}
	
	public void setLnCount(int lnCount) {
		this.lnCount = lnCount;
	}
	
	public int getLrCount() {
		return lrCount;
	}
	
	public void setLrCount(int lrCount) {
		this.lrCount = lrCount;
	}
	
	public int getDfCount() {
		return dfCount;
	}
	
	public void setDfCount(int dfCount) {
		this.dfCount = dfCount;
	}
	
	public int getNullCount() {
		return nullCount;
	}
	
	public void setNullCount(int nullCount) {
		this.nullCount = nullCount;
	}
	
	public int getCount() {
		return count;
	}
	
	public void setCount(int count) {
		this.count = count;
	}
	
}
